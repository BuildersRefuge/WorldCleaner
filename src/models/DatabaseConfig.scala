package models

class DatabaseConfig {
  /**
    * Database host address.
    */
  var host: String = "localhost"
  /**
    * Database username
    */
  var username: String = "username"
  /**
    * Database password
    */
  var password: String = "password"
  /**
    * Database port
    */
  var port: Int = 3306
  /**
    * Database name
    */
  var database: String = "database"
  /**
    * Database table
    */
  var table: String = "table"
}
